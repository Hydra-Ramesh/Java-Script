// In JavaScript data types are classified into two categories: primitive and reference types.
// Primitive types: number, string, boolean, null, undefined, symbol
// Reference types: object

// In JavaScript Variables are declared using var, let, or const keyword.

var myVariable = "Hello World!"; // variable declared using var keyword for primitive types and for hosting variables
console.log(myVariable);

let myVariable2 = "Hello World!"; // variable declared using let keyword for block scope and for mutable variables
console.log(myVariable2);

const myVariable3 = "Hello World!"; // variable declared using const keyword for block scope and for immutable variables
console.log(myVariable3);