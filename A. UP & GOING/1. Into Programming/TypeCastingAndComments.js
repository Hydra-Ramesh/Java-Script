// We can convert the types in javascript using Type Casting

let num = "10";
console.log("Before type casting: ", num);
num = parseInt(num);
console.log("After type casting: ", num);

// We can convert the types in javascript using Type Conversion

let str = "10";
console.log("Before type conversion: ", str);
str = Number(str);
console.log("After type conversion: ", str);

